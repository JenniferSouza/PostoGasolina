        //VARIAVEL PARA RECEBER VALOR DIGITADO
        String strQtdCombustivel = jTextFieldQtdCombustivel.getText();

        //VALOR FINAL
        double vfinal = 0;
        
        //SE QTD FOR=VAZIO
        if (strQtdCombustivel.equals("")){
            
        }
        //SE DISEL FOR SELECIONADO
        else if(jRadioButtonDiesel.isSelected()==true){
            modeloCombustivel.qtdLitros = Double.parseDouble(strQtdCombustivel);
            modeloCombustivel.totalPrecoCombustivel = modeloCombustivel.precoDiesel * modeloCombustivel.qtdLitros;
        }
        //SE ETANOL FOR SELECIONADO
        else if (jRadioButtonEtanol.isSelected()==true){
            modeloCombustivel.qtdLitros = Double.parseDouble(strQtdCombustivel);
            modeloCombustivel.totalPrecoCombustivel = modeloCombustivel.precoEtanol * modeloCombustivel.qtdLitros;
        }
        //SE GASOLINA FOR SELECIONADO
        else if (jRadioButtonGasolina.isSelected()==true){
            modeloCombustivel.qtdLitros = Double.parseDouble(strQtdCombustivel);
            modeloCombustivel.totalPrecoCombustivel = modeloCombustivel.precoGasolina * modeloCombustivel.qtdLitros;
        }
        
        
        jLabelTotalCombustivel.setText("R$ "+String.valueOf(modeloCombustivel.totalPrecoCombustivel));
        
        //ENVIAR PARA O TOTAL FINAL
        if(modeloServExtra.valorTotalVendaOleo == 0){
            jLabelTotalFinal.setText("R$ "+modeloCombustivel.totalPrecoCombustivel);
        }
        else{
            double totalVenda = modeloCombustivel.totalPrecoCombustivel + modeloServExtra.valorTotalVendaOleo;
            
            jLabelTotalFinal.setText("R$ "+String.valueOf(totalVenda));
        }


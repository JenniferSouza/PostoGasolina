    private void jComboBoxListaOleoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxListaOleoItemStateChanged
                        //SE A TROCA DE OLEO FOR SELECIONADA, FAÇA
        if (jCheckBoxTrocaOleo.isSelected()==true){
        
            //RECEBER OLEO SELECIONADO
            String oleoSelecionado = (String)jComboBoxListaOleo.getSelectedItem();
            //VERIFICAR QUAL OLEO FOI SELECIONADO
            if(oleoSelecionado.equals("SELECIONE")){
                //SETAR ZERO
                jLabelPrecoOleo.setText("0");
                
            }
            else if(oleoSelecionado.equals("LUBRAX")){
                //EXIBIR VALOR DO LUBRAX
                jLabelPrecoOleo.setText("R$ "+String.valueOf(modeloServExtra.valorLubrax));
                jButtonCarrinhoOleo.setVisible(true);
            }
            else if(oleoSelecionado.equals("SHELL")){
                //EXIBIR VALOR DO SHELL
                jLabelPrecoOleo.setText("R$ "+String.valueOf(modeloServExtra.valorShell));
                jButtonCarrinhoOleo.setVisible(true);
            }
            else if(oleoSelecionado.equals("CASTROL")){
                //EXIBIR VALOR DO CASTROL
                jLabelPrecoOleo.setText("R$ "+String.valueOf(modeloServExtra.valorCastrol));
                jButtonCarrinhoOleo.setVisible(true);
            }
            
            else if(oleoSelecionado.equals("IPIRANGA")){
                //EXIBIR VALOR DO IPIRANGA
                jLabelPrecoOleo.setText("R$ "+String.valueOf(modeloServExtra.valorIpiranga));
                jButtonCarrinhoOleo.setVisible(true);
            }
        
        }


        // TODO add your handling code here:
    }//GEN-LAST:event_jComboBoxListaOleoItemStateChanged

    private void jButtonCarrinhoOleoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCarrinhoOleoActionPerformed

        //EXIBIR TOTAL DO SERVICO EXTRA
        int qtdOleo = Integer.parseInt(jFormattedTextFieldQtdOleo.getText());
        double precoOleo = Double.parseDouble(jLabelPrecoOleo.getText().replace("R$ ", ""));
        
        //RECEBER VALOR TOTAL DOS SERVICOS REALIZADOS
        double vtotal = 0;
        
        modeloServExtra.valorTotalVendaOleo = qtdOleo * precoOleo;
 
        if(modeloCombustivel.totalPrecoCombustivel == 0){
            
            jLabelTotalFinal.setText("R$ "+String.valueOf(modeloServExtra.valorTotalVendaOleo));
            jLabelTotalServExtra.setText("R$ "+modeloServExtra.valorTotalVendaOleo);
            modeloServExtra.nomeOleo = (String)jComboBoxListaOleo.getSelectedItem();
            modeloServExtra.qtdOleo = qtdOleo;
            
        }else{
            
            vtotal = modeloServExtra.valorTotalVendaOleo + modeloCombustivel.totalPrecoCombustivel;            
            jLabelTotalFinal.setText("R$ "+String.valueOf(vtotal));
            
            modeloServExtra.nomeOleo = (String)jComboBoxListaOleo.getSelectedItem();
            modeloServExtra.qtdOleo = qtdOleo;
            
            jLabelTotalServExtra.setText("R$ "+modeloServExtra.valorTotalVendaOleo);
        }
        

        // TODO add your handling code here:
    }//GEN-LAST:event_jButtonCarrinhoOleoActionPerformed

